公平承担输配电费、系统运行费、政策性交叉补贴等费用。
项目主责单位负责统一与公共电网结算电费。
并网型和离网型项目应按现行政策缴纳政府性基金及附加。
项目新能源发电量不纳入新能源可持续发展价格结算机制。

【第六条：绿电溯源】
并网型项目整体按照内部实际新能源发电量（含储能释放的
项目新能源电量）扣减上网电量确定自发自用电量，
